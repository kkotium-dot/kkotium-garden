// 배지 우선순위 — SINGLE authority (작업원칙 #274, #233, #62).
//
// 상품 행 이름칸에 배지가 최대 7개까지 쌓여 행 높이가 178px가 됐다. 좁은 화면에서
// 뭉치고, 매일 보는 목록이라 스캔 비용이 그대로 운영 비용이 된다.
//
// 해법은 "배지를 지우는 것"이 아니라 "순서를 정하는 것"이다. 모든 배지는 각자
// 필요해서 생겼으므로 삭제하면 정보가 사라진다. 대신 상시 노출은 상위 N개로
// 제한하고 나머지는 접는다 — 정보 손실 0, 스캔 비용만 감소.
//
// 순서의 기준은 **돈이 새는 순서**다. 디자인 취향이나 구현 편의가 아니라,
// "이 배지를 못 보고 지나치면 얼마를 잃는가"로 줄을 세운다:
//
//   1) 사고   — 주문 취소·자산 영구 소멸처럼 되돌릴 수 없는 손실
//   2) 매출   — 마진 붕괴·좀비화처럼 지금 돈이 새고 있는 상태
//   3) 신뢰   — 앱↔네이버 불일치처럼 판단 근거 자체가 틀어진 상태
//   4) 상태   — 발행/판매 상태 같은 사실 정보 (알면 좋지만 급하지 않음)
//   5) 개선   — 상품명 등급처럼 "언젠가 손보면 좋은" 여지
//   6) 메타   — 출처 태그처럼 분류용 정보
//
// 숫자는 10 단위로 띄워 뒀다 — 새 배지가 생겨도 기존 값을 건드리지 않고 사이에
// 끼워 넣기 위함(#62: 한 곳만 고치면 전 화면이 같이 움직인다).
// 전상품·전화면 범용(#55).
// ============================================================================

